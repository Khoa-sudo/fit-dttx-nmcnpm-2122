
// Chuyễn đổi kiểu dữ liệu cho biến

let number = 100; // integer
console.log(typeof number);
console.log(`number = ${number}`);

number = "Hello"; // string
console.log(typeof number);
console.log(`number = ${number}`);

//chuyển string thành integer
number = parseInt("123.45")
console.log('(int) number = ',number);

//chuyển string thành float
number = parseFloat("123.45")
console.log('(float) number = ',number);